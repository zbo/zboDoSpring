public class Student {
    private String ID;
    private int age;
    private int score;
    private String name;

    public String getID() {
        return ID;
    }

    public int getAge() {
        return age;
    }

    public int getScore() {
        return score;
    }

    public String getName() {
        return name;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public void setName(String name) {
        this.name = name;
    }
}
